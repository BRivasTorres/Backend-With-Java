public class VariablesTipes {
    public static void main(String[] args) {
        System.out.println("Tipos de variables");
        // *Para valores enteros */
        int age = 30;
        System.out.println(age);
        // *Para valores decimales */
        double salary = 1250.50;
        System.out.println(salary);

        // *Este proceso de convertir un dato a otro se le llama Cast */
        double var1 = 2309.38;
        System.out.println(var1);
        int var1Enter = (int) var1;
        System.out.println(var1Enter);

        long test = 484824323L;
        System.out.println(test);
    }
}
